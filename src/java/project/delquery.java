public class delquery extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        PrintWriter out = response.getWriter();
        try  {
            String enrollno=request.getParameter("enrollno");
            String qry=request.getParameter("qry");
            
            Class.forName("com.mysql.jdbc.Driver");
  	    Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/hostel","root","123");
            Statement stmt=con.createStatement();
            
            int rs=stmt.executeUpdate("delete from query where enrollno='"+enrollno+"' and qry='"+qry+"'");
            
            JOptionPane.showMessageDialog (null, "Query Deleted", "Done", JOptionPane.INFORMATION_MESSAGE);
            response.sendRedirect("viewquery");
        }
        catch(Exception e){out.print(e);}

    }
}